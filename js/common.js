$(document).ready(function() {

	$('.navigation__point').click(function(){
		$('.navigation-hidden').toggleClass('is-open');
		return false;
	});

	$('.nav-foot__link').click(function(){
		$('.nav-foot__content').toggleClass('is-open');
		return false;
	});

	$('.btn-plus').click(function(){
		$('.nav-foot__content').toggleClass('is-open');
		return false;
	});














});



